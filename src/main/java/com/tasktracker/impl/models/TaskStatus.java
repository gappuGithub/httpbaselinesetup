package com.tasktracker.impl.models;

/**
 * Enumeration of possible task statuses.
 */
public enum TaskStatus {
    /**
     * Task is yet to be started
     */
    TODO,
    
    /**
     * Task is currently being worked on
     */
    IN_PROGRESS,
    
    /**
     * Task has been completed
     */
    DONE
}

